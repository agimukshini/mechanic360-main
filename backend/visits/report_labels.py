"""
Localized strings for PDF reports and report API errors.

Workshop language is stored on the tenant (`sq` default, `en` optional).
"""
from __future__ import annotations

SUPPORTED_LANGUAGES = frozenset({"sq", "en"})


def normalize_language(code: str | None) -> str:
    if not code:
        return "sq"
    base = str(code).lower().split("-")[0]
    return base if base in SUPPORTED_LANGUAGES else "sq"


