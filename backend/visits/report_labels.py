def get_labels(bundle: dict[str, dict[str, str]], language: str | None) -> dict[str, str]:
    lang = normalize_language(language)
    return bundle.get(lang, bundle["sq"])


def api_error(key: str, language: str | None) -> str:
    labels = get_labels(API_ERRORS, language)
    return labels.get(key, key)
